//splitting a sentence into words 
void split(vector<string> dataLoad) 
{
	string word = " ";
	for (auto x : dataLoad) {
		if (x == " ") {
			cout << word << endl;

		}
		else {
			word = word + x;
		}
	}
	cout << word << endl;
}

